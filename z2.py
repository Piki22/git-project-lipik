a= True
b= False

if a or b:
    print("a or b= ", True)
else:
    print("a or b= ", False)

if a and b:
    print("a and b= ", True)
else:
    print("a and b= ", False)

if not a or b:
    print("not a or b= ", True)
else:
    print("not a or b= ", False)

if not a and b:
    print("not a and b= ", True)
else:
    print("not a and b= ", False)